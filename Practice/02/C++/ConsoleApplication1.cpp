#include <iostream>

using namespace std;

int main()
{
    int a1 = 3;
    int a2 = 3.14;

    double b1 = 3;
    double b2 = 3.14;

    cout << a1 << endl;
    cout << a2 << endl;
    // переменные типа int хранят целые значения чисел
    cout << b1 << endl;
    cout << b2 << endl;
    // переменные типа double могут принимать более точные значения
   
    return 0;
}